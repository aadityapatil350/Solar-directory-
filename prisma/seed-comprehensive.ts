async function main() {
  console.log('Starting database seed...');

  // Create Categories
  console.log('Creating categories...');
  const createdCategories = await Promise.all(
    categories.map((cat) =>
      prisma.category.upsert({
        where: { slug: cat.slug },
        update: {},
        create: {
          name: cat.name,
          slug: cat.slug,
        },
      })
    )
  );

  // Create Locations
  console.log('Creating locations...');
  const createdLocations = await Promise.all(
    cities.map((city) =>
      prisma.location.upsert({
        where: { slug: city.slug },
        update: {},
        create: {
          city: city.city,
          state: city.state,
          slug: city.slug,
        },
      })
    )
  );

  // Create Listings
  console.log('Creating listings...');
  let count = 0;
  for (const listing of allListings) {
    try {
      await prisma.listing.upsert({
        where: { slug: listing.slug },
        update: {},
        create: {
          name: listing.name,
          slug: listing.slug,
          description: listing.description || null,
          phone: listing.phone,
          email: listing.email || null,
          website: listing.website || null,
          address: listing.address || null,
          verified: listing.verified,
          featured: listing.featured,
          rating: listing.rating,
          reviews: listing.reviews,
          categoryId: createdCategories[listing.categoryIndex].id,
          locationId: createdLocations[listing.cityIndex].id,
        },
      });
      count++;
      console.log(`Created listing ${count}/${allListings.length}: ${listing.name}`);
    } catch (error) {
      console.error(`Error creating ${listing.name}:`, error);
    }
  }

  console.log(`\nâœ… Database seeded successfully!`);
  console.log(`   - ${createdCategories.length} categories`);
  console.log(`   - ${createdLocations.length} locations`);
  console.log(`   - ${count} listings`);
}

main()
  .catch((e) => {
    console.error('Error seeding database:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
